from math import sin, cos, tan, radians

print("----------------------------------------------------------------------------------------------------------")
print("SENO - COSSENO - TANGENTE")
print("----------------------------------------------------------------------------------------------------------")

angle = float(input("Ângulo(°): "))

sine = sin(radians(angle))
cosine = cos(radians(angle))
tangent = tan(radians(angle))

print("----------------------------------------------------------------------------------------------------------")

print("Seno: {0:.2f}".format(sine))
print("Cosseno: {0:.2f}".format(cosine))
print("Tangente: {0:.2f}".format(tangent))

print("----------------------------------------------------------------------------------------------------------")
print("Encerrando...")
print("----------------------------------------------------------------------------------------------------------")
